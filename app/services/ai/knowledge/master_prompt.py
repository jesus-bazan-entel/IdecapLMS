def get_master_prompt() -> str:
    """Retorna el prompt maestro completo."""
    return MASTER_PROMPT

def get_audio_prompt(
    tema: str,
    nivel: str = "básico",
    contexto: str = "",
    voz_es: str = "Dalia",
    voz_pt: str = "Francisca"
) -> str:
    """Genera el prompt para contenido de Audio TTS."""
    return AUDIO_TTS_TEMPLATE.format(
        tema=tema,
        nivel=nivel,
        contexto=contexto or f"Situación cotidiana relacionada con {tema}",
        voz_es=voz_es,
        voz_pt=voz_pt
    )

def get_presentation_prompt(
    tema: str,
    nivel: str = "básico",
    num_slides: int = 10,
    enfoque: str = "vocabulario"
) -> str:
    """Genera el prompt para presentaciones."""
    return PRESENTATION_TEMPLATE.format(
        tema=tema,
        nivel=nivel,
        num_slides=num_slides,
        enfoque=enfoque,
        num_slides_menos_2=num_slides - 2,
        num_slides_menos_1=num_slides - 1
    )

def get_mindmap_prompt(
    tema: str,
    nivel: str = "básico",
    enfoque: str = "vocabulario relacionado"
) -> str:
    """Genera el prompt para mapas mentales."""
    return MINDMAP_TEMPLATE.format(
        tema=tema,
        nivel=nivel,
        enfoque=enfoque
    )

def get_podcast_prompt(
    tema: str,
    nivel: str = "intermedio",
    duracion: int = 10,
    formato: str = "conversacional",
    participante_adicional: str = "Invitado experto"
) -> str:
    """Genera el prompt para podcasts."""
    desarrollo_min = duracion - 5  # Resto del tiempo después de intro, práctica y outro
    return PODCAST_TEMPLATE.format(
        tema=tema,
        nivel=nivel,
        duracion=duracion,
        formato=formato,
        participante_adicional=participante_adicional,
        desarrollo_min=desarrollo_min
    )

def get_video_prompt(
    tema: str,
    nivel: str = "básico",
    tipo_video: str = "tutorial",
    duracion: int = 5
) -> str:
    """Genera el prompt para videos."""
    contenido_principal_min = duracion - 2  # Tiempo restante después de intro y outro
    return VIDEO_TEMPLATE.format(
        tema=tema,
        nivel=nivel,
        tipo_video=tipo_video,
        duracion=duracion,
        contenido_principal_min=contenido_principal_min,
        graficos=f"explicar {tema}",
        imagenes=f"contexto de {tema}"
    )


